public class DomainException extends Exception {

	/**
	 * Default serialVersionUID
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * Constructs a domain exception by constructing a Exception object with
	 * default message.
	 */
	public DomainException() {
		super("Domain error");
	}

	/**
	 * Constructs a domain exception with given error message.
	 * 
	 * @param msg
	 *            error message
	 */
	public DomainException(String msg) {
		super(msg);
	}
}
