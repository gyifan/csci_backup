public class Address {

	// Instance fields
	private String street;
	private String city;
	private String state;
	private String zipCode;

	/**
	 * Constructs an Address object which contains the given information of
	 * street, city, state, and the zip code.
	 * 
	 * @param street
	 *            the street name
	 * @param city
	 *            the city name
	 * @param state
	 *            the state name
	 * @param zipCode
	 *            the zip code of the address
	 */
	public Address(String street, String city, String state, String zipCode) {
		this.street = street;
		this.city = city;
		this.state = state;
		this.zipCode = zipCode;
	}

	/**
	 * Gets the street information.
	 * 
	 * @return the street
	 */
	public String getStreet() {
		return street;
	}

	/**
	 * Sets the street info to the given street.
	 * 
	 * @param street
	 *            the street to set
	 */
	public void setStreet(String street) {
		this.street = street;
	}

	/**
	 * Gets the city information.
	 * 
	 * @return the city
	 */
	public String getCity() {
		return city;
	}

	/**
	 * Sets the city info to the given city.
	 * 
	 * @param city
	 *            the city to set
	 */
	public void setCity(String city) {
		this.city = city;
	}

	/**
	 * Gets the state info.
	 * 
	 * @return the state
	 */
	public String getState() {
		return state;
	}

	/**
	 * Sets the state info to the given state.
	 * 
	 * @param state
	 *            the state to set
	 */
	public void setState(String state) {
		this.state = state;
	}

	/**
	 * Gets the zip code of the address.
	 * 
	 * @return the zipCode
	 */
	public String getZipCode() {
		return zipCode;
	}

	/**
	 * Sets the zip code to the given zip code.
	 * 
	 * @param zipCode
	 *            the zipCode to set
	 */
	public void setZipCode(String zipCode) {
		this.zipCode = zipCode;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	public String toString() {
		return "Street: " + street + "\nCity: " + city + "\nState: " + state
				+ "\nZip Code: " + zipCode + "\n";
	}

	/**
	 * Tests the Address class
	 * 
	 * @param args
	 *            unused
	 */
	public static void main(String[] args) {
		Address myAddr = new Address("123 Sesame Street", "Park City", "PA",
				"17837");
		System.out.println(" testing the address ... ");
		System.out.println(myAddr);
		System.out
				.println("The address should be...\nStreet: 123 Sesame Street\nCity: Park City\nState: PA\nZip Code: 17837\n");

	}
}
